class IfExample{
    public static void main(String[] args){
        int number = 10;
        //Switch expression  
        switch(number){
            //Case statements  
            case 10:
            {
                System.out.println("Hello 10");
                break;
            }
            case 20:
            {
                System.out.println("Hello 20");
                break;
            }
            case 30:
            {
                System.out.println("Hello 30");
                break;
            }
            //Default case statement  
            default:
            {
                System.out.println("Hello");
            }
        }

    }
}